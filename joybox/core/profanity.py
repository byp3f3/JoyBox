# -*- coding: utf-8 -*-
"""
Проверка текста на нецензурную лексику.
Список слов можно расширить в PROFANITY_WORDS или загрузить из файла.
"""
import re

# Минимальный список нецензурных слов (нижний регистр). Расширьте по необходимости.
PROFANITY_WORDS = frozenset([
    'бля', 'блять', 'блядь', 'хуй', 'хуя', 'пизда', 'хуйня', 'пизду', 'ебать', 'ебал', 'ёбаный',
    'сука', 'суки', 'мудак', 'мудила', 'дерьмо', 'гавно', 'жопа', 'залупа', 'хер',
    'нахер', 'похер', 'блядина', 'блядский', 'ебаный', 'ёб', 'выблядок', 'ублюдок',
])


def contains_profanity(text):
    """
    Проверяет, содержится ли в тексте нецензурная лексика.
    :param text: строка для проверки
    :return: True если найдено запрещённое слово, иначе False
    """
    if not text or not text.strip():
        return False
    # Нормализуем: нижний регистр, разбиваем на слова (буквы и цифры)
    normalized = re.sub(r'[^\w\s]', ' ', text.lower())
    words = set(re.findall(r'[а-яёa-z0-9]+', normalized))
    return bool(words & PROFANITY_WORDS)
